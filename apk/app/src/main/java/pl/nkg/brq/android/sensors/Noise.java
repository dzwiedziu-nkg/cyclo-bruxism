package pl.nkg.brq.android.sensors;

import android.media.MediaRecorder;

import java.io.IOException;

public class Noise {

    private static final String TAG = Noise.class.getSimpleName();

    private MediaRecorder mRecorder;

    public void startRecorder() throws SecurityException {
        if (mRecorder == null) {
            mRecorder = new MediaRecorder();
            mRecorder.setAudioSource(MediaRecorder.AudioSource.MIC);
            mRecorder.setOutputFormat(MediaRecorder.OutputFormat.THREE_GPP);
            mRecorder.setAudioEncoder(MediaRecorder.AudioEncoder.AMR_NB);
            mRecorder.setOutputFile("/dev/null");
            try {
                mRecorder.prepare();
                mRecorder.start();
            } catch (IOException e) {
                throw new RuntimeException("Can't write to /dev/null", e);
            }
        }
    }

    public void stopRecorder() {
        if (mRecorder != null) {
            mRecorder.stop();
            mRecorder.release();
            mRecorder = null;
        }
    }

    public double getNoise() {
        return 20 * Math.log10(getAmplitude());
    }

    private double getAmplitude() {
        if (mRecorder != null)
            return (mRecorder.getMaxAmplitude());
        else
            return 0;

    }
}
